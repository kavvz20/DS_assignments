#include <iostream>
using namespace std;

void heapify(int a[], int n, int i, bool isMax) {
    int largest = i, left = 2*i + 1, right = 2*i + 2;

    if (isMax) {
        if (left < n && a[left] > a[largest]) largest = left;
        if (right < n && a[right] > a[largest]) largest = right;
    } else {
        if (left < n && a[left] < a[largest]) largest = left;
        if (right < n && a[right] < a[largest]) largest = right;
    }

    if (largest != i) {
        swap(a[i], a[largest]);
        heapify(a, n, largest, isMax);
    }
}

void heapSort(int a[], int n, bool increasing) {
    bool isMax = increasing;

    for (int i = n/2 - 1; i >= 0; i--)
        heapify(a, n, i, isMax);

    for (int i = n - 1; i >= 0; i--) {
        swap(a[0], a[i]);
        heapify(a, i, 0, isMax);
    }
}

int main() {
    int n;
    cout << "Enter size: ";
    cin >> n;

    int a[n];
    cout << "Enter elements: ";
    for (int i = 0; i < n; i++) cin >> a[i];

    int choice;
    cout << "1: Increasing, 2: Decreasing : ";
    cin >> choice;

    heapSort(a, n, choice == 1);

    cout << "Sorted array: ";
    for (int i = 0; i < n; i++) cout << a[i] << " ";
}
